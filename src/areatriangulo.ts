function calcularArea(base, altura) {
  let areaDelTriangulo = (base * altura) / 2;
  return `El area es ${areaDelTriangulo}`;
}

console.log(calcularArea(1, 2));
console.log(calcularArea(2, 4));
console.log(calcularArea(3, 6));
console.log(calcularArea(4, 8));
console.log(calcularArea(5, 10));
console.log(calcularArea(6, 12));
